# "이 함수의 이름(함수 이름)은 add이고 입력으로 2개의 값을 받으며 결과값은 2개의 입력값을 더한 값이다"
# 여기에서 return은 함수의 결과값을 돌려주는 명령어이다. 먼저 다음과 같이 add함수를 만들자.
